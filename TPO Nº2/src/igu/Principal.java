    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // Guardar Servicio/Cliente/Mascota
        String nombreCliente=txtNombreDuenio.getText();
        String celCliente=txtCelDuenio.getText();
        String nombreMascota=txtNombreMascota.getText();
        String raza=txtRaza.getText();
        String color=txtColor.getText();
        String tareasObservaciones=txtAreaTareas.getText();        
        int alergico=cmboxAlergico.getSelectedIndex();
        int atencionEspecial=cmboxAtenEspecial.getSelectedIndex();
        try {
            control.guardarServicio(nombreCliente, celCliente, nombreMascota, raza, color, tareasObservaciones, alergico, atencionEspecial);
            JOptionPane.showMessageDialog(panelCliente, "Servicio guardado", "Guardar", JOptionPane.INFORMATION_MESSAGE);
            limpiar();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(panelCliente, e.getMessage(), "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

